const revealItems = document.querySelectorAll('.reveal');
const langButtons = document.querySelectorAll('.lang-button');
const translatableNodes = document.querySelectorAll('[data-i18n]');
const placeholderNodes = document.querySelectorAll('[data-i18n-placeholder]');
const metaDescription = document.querySelector('meta[name="description"]');

const observer = new IntersectionObserver(
  entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  },
  {
    threshold: 0.2,
  }
);

revealItems.forEach(item => observer.observe(item));

document.getElementById('year').textContent = new Date().getFullYear();

const contactForm = document.querySelector('.contact-form');
const submitButton = contactForm.querySelector('button');

function applyLanguage(lang) {
  const locale = translations[lang] || translations.en;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.title = locale.pageTitle;
  metaDescription.setAttribute('content', locale.pageDescription);

  translatableNodes.forEach(node => {
    const key = node.dataset.i18n;
    if (locale[key]) {
      node.textContent = locale[key];
    }
  });

  placeholderNodes.forEach(node => {
    const key = node.dataset.i18nPlaceholder;
    if (locale[key]) {
      node.setAttribute('placeholder', locale[key]);
    }
  });

  if (submitButton.disabled) {
    submitButton.textContent = locale['contact.sent'];
  }

  langButtons.forEach(button => {
    button.classList.toggle('active', button.dataset.lang === lang);
  });

  localStorage.setItem('zeeeeee-language', lang);
}

langButtons.forEach(button => {
  button.addEventListener('click', () => {
    applyLanguage(button.dataset.lang);
  });
});

applyLanguage(localStorage.getItem('zeeeeee-language') || 'en');

contactForm.addEventListener('submit', event => {
  event.preventDefault();
  const currentLang = document.documentElement.lang === 'ar' ? 'ar' : 'en';
  submitButton.textContent = translations[currentLang]['contact.sent'];
  submitButton.disabled = true;
});
